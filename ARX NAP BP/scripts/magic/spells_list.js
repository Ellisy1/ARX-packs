// Создание списка шифровки заклинаний
import { runeCiphers } from './rune_cipher_list'

export let spellsCiphers = [] // spellsCiphers представляет собой подобие spellsList, только вместо объекта представлен массив с шифровкой каждого заклинания

for (const spell in spellsList) {
    // Получаем список последовательности рун в заклинании в list_of_runes
    const list_of_runes = spell.split(" ")

    let currentSpellCipher = ""
    // Перебираем каждую руну
    for (const rune of list_of_runes) {
        if (!(rune in runeCiphers)) { // Если нет указаной руны
            console.error(`Не существует указаной в заклинаниях руны <${rune}> в заклинании <${spell}>`)
        }
        else { // Руна есть, записываем её шифровку
            currentSpellCipher = runeCiphers[rune] + currentSpellCipher
        }
    }

    // Проверяем корреткность заклинания
    if (currentSpellCipher.length < 4) {
        console.warn(`Заклинание <${spell}> слишком короткое, что недопустимо по теории магии`)
    }
    if (!(currentSpellCipher.endsWith("AC") || currentSpellCipher.endsWith("AK") || currentSpellCipher.endsWith("AT") || currentSpellCipher.endsWith("AX"))) {
        console.warn(`Заклинание <${spell}> имеет ошибку составления в первой руне`)
    }

    spellsCiphers.push(currentSpellCipher)
}